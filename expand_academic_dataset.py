def _generate_rows(target_per_subject: int = 80) -> List[Tuple[str, str]]:
    templates = _templates()
    topics_map = _topics()
    out: List[Tuple[str, str]] = []
    for intent, topics in topics_map.items():
        for i in range(target_per_subject):
            t = topics[i % len(topics)]
            tmpl = templates[i % len(templates)]
            q = tmpl.format(t=t).strip()
            if not q.endswith("?"):
                q = q.rstrip(".") + "?"
            out.append((q, intent))
    return out


def main() -> None:
    existing: List[Tuple[str, str]] = []
    seen: Set[str] = set()
    with open(CSV_PATH, "r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            q = (row.get("question") or "").strip()
            intent = (row.get("intent") or "").strip()
            if not q or not intent:
                continue
            key = q.lower()
            if key in seen:
                continue
            seen.add(key)
            existing.append((q, intent))

    new_rows = _generate_rows(target_per_subject=80)
    added = 0
    for q, intent in new_rows:
        key = q.lower()
        if key in seen:
            continue
        seen.add(key)
        existing.append((q, intent))
        added += 1

    with open(CSV_PATH, "w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f, lineterminator="\n")
        writer.writerow(["question", "intent"])
        writer.writerows(existing)

    print(f"[OK] Wrote {CSV_PATH}")
    print(f"     Previous unique rows kept: {len(existing) - added}")
    print(f"     New rows added: {added}")
    print(f"     Total rows: {len(existing)}")


if __name__ == "__main__":
    main()
